#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n > 0 && *s1 && *s2 && n--)
		if (*s1++ != *s2++)
			return (*(t_uc *)(s1 - 1) - *(t_uc *)(s2 - 1));
	if (n == 0)
		return (0);
	return (*(t_uc *)s1 - *(t_uc *)s2);
}
